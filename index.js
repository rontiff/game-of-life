//Initialize/reset the board state

function  init() {
    for (let i = 0; i < columns; i++) {
        for (let j = 0; j < rows; j++) {
            //currentBoard[i][j] = random() > 0.8 ? 1 : 0;
            currentBoard[i][j] =  0;
            nextBoard[i][j] = 0;
        }
    }
}
// similar to initial 
function  ranInit() {
    for (let i = 0; i < columns; i++) {
        for (let j = 0; j < rows; j++) {
            currentBoard[i][j] = random() > 0.8 ? 1 : 0;

        }
    }
}



function draw() {
    background(backgroundColor);
    let switchcolor=document.querySelector('#switchcolor').value;
    generate();

    for (let i = 0; i < columns; i++) {
        for (let j = 0; j < rows; j++) {
            let random1=Math.floor(Math.random()*255)
            let random2=Math.floor(Math.random()*255)
            let random3=Math.floor(Math.random()*255)
            if (currentBoard[i][j] == 1){
                if (switchcolor==1){
                fill(boxColor); 
                }else if(switchcolor==2){
                fill(random1,random2,random3);
                }   
            } else {
                fill(backgroundColor);
            } 
            stroke(strokeColor);
            rect(i * unitLength, j * unitLength, unitLength, unitLength);
        }
    }
}

function generate() {
    //Loop over every single box on the board
    for (let x = 0; x < columns; x++) {
        for (let y = 0; y < rows; y++) {
            // Count all living members in the Moore neighborhood(8 boxes surrounding)
            let neighbors = 0;
            for (let i of [-1, 0, 1]) {
                for (let j of [-1, 0, 1]) {
                    if( i == 0 && j == 0 ){
                        // the cell itself is not its own neighbor
                        continue;
                    }
                    // The modulo operator is crucial for wrapping on the edge
                    neighbors += currentBoard[(x + i + columns) % columns][(y + j + rows) % rows];
                }
            }

            let dieloneliness = Math.floor(document.querySelector('#survival').value);
            let dieoverpopulation= Math.floor(document.querySelector('#overproduction').value);
            let reproduction= Math.floor(document.querySelector('#reproduction').value);


            // Rules of Life
            if (currentBoard[x][y] == 1 && neighbors < dieloneliness) {
                // Die of Loneliness
                nextBoard[x][y] = 0;
            } else if (currentBoard[x][y] == 1 && neighbors >dieoverpopulation) {
                // Die of Overpopulation
                nextBoard[x][y] = 0;
            } else if (currentBoard[x][y] == 0 && neighbors == reproduction) {
                // New life due to Reproduction
                nextBoard[x][y] = 1;
            } else {
                // Stasis
                nextBoard[x][y] = currentBoard[x][y];
            }
        }
    }

    // Swap the nextBoard to be the current Board
    [currentBoard, nextBoard] = [nextBoard, currentBoard];
}



/**
 * When mouse is dragged
 */
 function mouseDragged() {
    /**
     * If the mouse coordinate is outside the board
     */
    if (mouseX > unitLength * columns || mouseY > unitLength * rows) {
        return;
    }
    const x = Math.floor(mouseX / unitLength);
    const y = Math.floor(mouseY / unitLength);

    console.log(x,y)
    currentBoard[x][y] = 1;
    fill(boxColor);
    stroke(strokeColor);
    rect(x * unitLength, y * unitLength, unitLength, unitLength);
}

/**
 * When mouse is pressed
 */
function mousePressed() {
    noLoop();
    mouseDragged();
}

